<h1>Joining the Library</h1>

<p>Joining our library is free to everyone, young or old, living, working or in full-time education in Niagara Falls, Fort Erie, Niagara-on-the-Lake, Pelham, Thorold and Welland. To become a member of the library all you have to do is complete an application form at the library or online and provide proof of identity (your name and address) in one of the following forms: Driving Licence; Electricity/Gas Bill; Pension/Benefits Book; Bank Statement; Passport; Student ID(with photo)</p>

<p>If you apply to join online you will be able to reserve items as soon as you have registered, but you will need to show proof of name and address at your local library in order to collect your membership card and borrow any reserved items.</p>

<p>At your library with your membership card you can:</p>
<ul>
    <li>Borrow up to 50 items for two weeks at a time</li>
    <li>Renew items</li>
    <li>Benefit from free access to paid for reference sources such as Rosetta Stone and the family history website Ancestry Library Edition.</li>
</ul>
From home you can use your library card to:

    Renew items by phone or online
    Access a range of online reference sources and search our online catalogue
    Request books from our catalogue by phone or online

<p>You will need your library card number and the PIN which goes with it to log on to library computers and for online transactions.</p>

Keeping track of your loans:

Please be aware that Flintshire Library and Information Service does not send out reminders about overdue items to adults or children. To avoid paying fines you can renew your items online or by telephone or mail.

